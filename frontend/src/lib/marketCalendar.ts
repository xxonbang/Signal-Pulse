/** "YYYY-MM-DD" 형식 날짜가 거래일인지 확인 */
export function isMarketOpen(dateStr: string): boolean {
  const d = new Date(dateStr + 'T00:00:00');
  const day = d.getDay(); // 0=일, 6=토
  if (day === 0 || day === 6) return false;
  return !KRX_HOLIDAYS.has(dateStr);
}
